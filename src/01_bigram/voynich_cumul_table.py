def sample_next(context: str, rng=None) -> str:
    """context の次の文字を累積確率表からサンプリングする。"""
    if rng is None: rng = random
    r = rng.random()
    for char, prob, cumul in VOYNICH_CUMUL[context]:
        if r <= cumul:
            return char
    return VOYNICH_CUMUL[context][-1][0]  # fallback

def generate_word(max_len: int = 12, rng=None) -> str:
    """Voynich風の語を1語生成する。'$' で語末。"""
    word = []
    c = sample_next('^', rng)
    while c != '$' and len(word) < max_len:
        word.append(c)
        c = sample_next(word[-1], rng)
    return ''.join(word)

